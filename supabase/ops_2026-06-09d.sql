WITH board(no, topic) AS (
  VALUES
    (1,'한국형 AI 동화책 제작 앱'),
    (2,'문화재 AI 해설 앱'),
    (4,'자격증 취약점 분석 학습 앱'),
    (5,'청년지원정책 안내 챗봇'),
    (6,'AI 자기소개서·면접 코치'),
    (7,'회복탄력성 루틴 코치'),
    (8,'내 아이 근시 관리용 플랫폼'),
    (9,'AI 창업 아이템 코치'),
    (10,'AI 동화책 제작 앱 개발'),
    (11,'회복탄력성 루틴 코치 (2팀)'),
    (12,'JD 기반 채용 진단 서비스'),
    (13,'외국인 관광객 맞춤형 실시간 맛집 동선 가이드 앱'),
    (14,'육猫(묘)일기(고양이 생애주기 앱)'),
    (15,'청년 AI 리터러시 격차 진단 및 정책 수요 예측 모델'),
    (16,'지친 마음에 따뜻한 말을 클릭 한번으로 - 위로·격려 챗 앱'),
    (17,'💊영양제 알리미💊'),
    (18,'사용자가 뉴스를 통해 원하는 분야(한국어 문해력, 영어, IT 등)의 핵심 용어와 어휘를 매일 꾸준히 학습하는 서비스.'),
    (19,'매일 서로에게 힘이 되는 칭찬 커뮤니티'),
    (20,'팬의 응원을 작가의 창작 자원으로 전환하는 예술 현물 후원 어플'),
    (21,'진료실 밖에서도 이어지는 정신과 케어 — AI 증상 기록·임상 리포트 플랫폼')
)
UPDATE rest_teams t
SET name = b.no || '팀'
FROM board b
WHERE regexp_replace(t.project_topic, '\s', '', 'g') = regexp_replace(b.topic, '\s', '', 'g')
  AND t.name <> b.no || '팀';

