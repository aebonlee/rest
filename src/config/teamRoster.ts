// ── 타입(인터페이스) 정의 ──
// interface(인터페이스)란? "이 데이터는 이런 모양이어야 한다"고 약속하는 설계도.
// 아래 RosterTeam 은 "한 팀을 표현하는 객체는 반드시 no, topic, members 를
// 가져야 하고 각각의 자료형은 이렇다"고 정해둔다. 실제 값이 아니라 '모양'만 정의한다.
// 한 팀(로스터 항목)의 데이터 구조를 정의하는 인터페이스.
export interface RosterTeam {
  no: number;        // 팀 번호(= 투표 순위/프로젝트 번호). 1부터 시작하는 고유 식별자.
                     // number = 숫자 자료형. "3"(글자)이 아니라 3(숫자)을 넣어야 한다.
  topic: string;     // 해당 팀이 진행하는 프로젝트 주제명.
                     // string = 글자(문자열) 자료형. 따옴표로 감싼 텍스트.
  members: string[]; // 팀 구성원 이름 목록(실제 계정명이 아닌 표시 이름 → 추후 계정 매칭 대상).
                     // string[] 의 [] 는 "배열(여러 개의 묶음)"이라는 뜻 → 글자 여러 개의 목록.
                     // 예) ['홍길동', '김철수'] 처럼 이름이 0개 이상 들어간다.
}

