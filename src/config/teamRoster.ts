// ── 표시용 이름 변환 헬퍼 함수 ──
// 팀 번호를 사람이 읽기 좋은 표시용 이름으로 변환한다.
// 매개변수: no(number) - 팀 번호.
// 반환값: `${no}팀` 형태의 문자열(예: 3 → "3팀"). 부수효과 없음(순수 함수).
//
// 개념 설명:
//  - "(no: number) => ..." 는 화살표 함수(arrow function) 문법이다.
//    "no 라는 숫자 하나를 받아서 => 오른쪽 결과를 돌려준다"는 뜻.
//    중괄호와 return 없이 한 줄로 쓰면, 그 식의 결과가 곧 반환값이 된다.
//  - 백틱(템플릿 리터럴) 문자열 안에서 달러+중괄호 표기를 쓰면 그 안의 값(여기선 no)을
//    글자로 끼워 넣어 준다. 예) no 가 3이면 결과 문자열은 "3팀" 이 된다.
//  - "순수 함수(pure function)"란? 같은 입력에는 항상 같은 출력을 내고,
//    바깥 세계(화면·서버 등)를 건드리지 않는 함수. 테스트하기 쉽고 예측 가능하다.
// 주의: 백틱 문자열 안에는 절대 주석을 넣지 말 것. 넣으면 그 글자가 그대로
//      출력 결과에 섞여 버린다. (그래서 설명은 이렇게 코드 밖에 적는다.)
export const teamName = (no: number) => `${no}팀`;
