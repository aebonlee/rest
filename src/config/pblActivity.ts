/**
 * 개인별 PBL활동 단계 정의 — 기본정보 + 컴퓨팅 사고 7단계 개발 워크시트(저장) + 루브릭(평가, 합계 100점).
 * 개발 프로젝트 흐름: 문제인식 → 문제정의 → 문제분해 → 추상화 → 알고리즘 설계 → 구현 → 결과 시연/발표.
 *
 * 콘텐츠는 koreatech 프로젝트활동(/assessment/project-*)의 워크시트를 그대로 이식하고,
 * 디자인은 snu PBL(좌측 사이드바 + 단계별 점수 + 자동평가 + DB 저장)을 유지한다.
 * 쉬었음 청년이 각 단계에서 처리한 내역(작성 내용)을 단계별로 확인할 수 있게 구성.
 */

export interface PblField {
  id: string;
  /** 사용자가 작성하는 작성란 라벨 (= koreatech 단계 title) */
  label: string;
  placeholder: string;
  /** 아래는 koreatech 워크시트의 풍부한 안내(선택). 있으면 카드형으로 렌더링한다. */
  no?: number;
  icon?: string;
  color?: string;
  desc?: string;
  guide?: string[];
  example?: string;
}

export interface PblStage {
  key: string;
  label: string;
  icon: string;
  color: string;
  /** 루브릭 배점 */
  max: number;
  desc: string;
  /** 평가 기준(루브릭) */
  rubric: string;
  /** 단계 개요(가이드 박스) */
  intro?: { lead: string; points: string[] };
  /** 예시에 사용한 프로젝트명 */
  exampleProject?: string;
  fields: PblField[];
}

