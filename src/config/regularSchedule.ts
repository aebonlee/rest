/**
 * 정규과정 Day별 수업 날짜 (주말·공강 제외). 총 15일(6/1~6/22).
 * regularTopics[i] ↔ REGULAR_DATES[i] 인덱스 1:1 매핑 (점검일 포함 15개, 순서 일치 필수).
 *
 * 일정 변경·공강 추가 시 이 배열만 수정하면 사이드바·"오늘" 표시가 자동 반영됩니다.
 */
// 배열 순서가 곧 Day 번호(인덱스 0 = Day 1). regularTopics 등 다른 배열과
// 인덱스로 짝지어지므로, 항목을 추가/삭제할 때 순서·개수를 반드시 함께 맞춰야 한다.
export const REGULAR_DATES: string[] = [
  '2026-06-01', // Day 1   (월)
  '2026-06-02', // Day 2   (화)  ← 오늘
  '2026-06-04', // Day 3   (목)   6/3(수) 공강
  '2026-06-05', // Day 4   (금) · 1차 기초점검일
  '2026-06-08', // Day 5   (월)
  '2026-06-09', // Day 6   (화)
  '2026-06-10', // Day 7   (수)
  '2026-06-11', // Day 8   (목)
  '2026-06-12', // Day 9   (금) · 2차 학습점검일
  '2026-06-15', // Day 10  (월)
  '2026-06-16', // Day 11  (화)
  '2026-06-17', // Day 12  (수)
  '2026-06-18', // Day 13  (목)
  '2026-06-19', // Day 14  (금)
  '2026-06-22', // Day 15  (월)  ← 마지막날, 총 15일
];

// 요일 인덱스 → 한글 약어 매핑 테이블.
// Date.getDay()가 0(일)~6(토)을 반환하므로 같은 순서로 배치되어 있어야 한다.
const WD = ['일', '월', '화', '수', '목', '금', '토'];

/** 'YYYY-MM-DD' → 'M/D(요일)' */
// 화면 표기용 변환기: '2026-06-05' → '6/5(금)' 형태로 짧게 보여준다.
export const fmtKDate = (iso: string): string => {
  // 문자열을 '-'로 분해 후 숫자화. 첫 요소(연도)는 사용하지 않으므로 구조분해에서 건너뛴다.
  // 예: '2026-06-05' → [, 6, 5] → m=6, d=5 (앞자리 0은 Number 변환으로 제거됨)
  const [, m, d] = iso.split('-').map(Number);
  // 시간대 이슈 회피를 위해 'T00:00:00'을 붙여 로컬 자정으로 파싱한 뒤 요일을 얻는다.
  // (날짜만 주면 UTC로 해석되어 하루 밀릴 수 있는 엣지케이스를 방지)
  const wd = WD[new Date(`${iso}T00:00:00`).getDay()];
  return `${m}/${d}(${wd})`;
};

/** 오늘 날짜를 'YYYY-MM-DD'(로컬)로 */
// "오늘" 강조 표시 등에서 REGULAR_DATES 항목과 문자열로 직접 비교하기 위한 헬퍼.
// toISOString()은 UTC 기준이라 자정 부근에 날짜가 틀어질 수 있어, 로컬 값으로 직접 조립한다.
export const todayISO = (): string => {
  const n = new Date();
  // 월/일을 항상 두 자리로 맞추는 0-패딩 함수(예: 6 → '06').
  const p = (x: number) => String(x).padStart(2, '0');
  // getMonth()는 0부터 시작하므로 +1 보정이 필요하다.
  return `${n.getFullYear()}-${p(n.getMonth() + 1)}-${p(n.getDate())}`;
};
