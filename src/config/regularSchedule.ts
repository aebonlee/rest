/**
 * 정규과정 Day별 수업 날짜 (주말·공강 제외). 총 15일(6/1~6/22).
 * regularTopics[i] ↔ REGULAR_DATES[i] 인덱스 1:1 매핑 (점검일 포함 15개, 순서 일치 필수).
 *
 * 일정 변경·공강 추가 시 이 배열만 수정하면 사이드바·"오늘" 표시가 자동 반영됩니다.
 *
 * [초보자 설명]
 *  - "배열(array)"은 값들을 순서대로 줄 세운 목록이다. 각 자리에는 0부터 번호(인덱스)가 붙는다.
 *    즉 REGULAR_DATES[0]은 첫 번째 값('2026-06-01'), REGULAR_DATES[1]은 두 번째 값이다.
 *  - 여기서는 "배열의 순서(인덱스)" 자체가 곧 'Day 번호'를 의미한다.
 *    인덱스 0 = Day 1, 인덱스 1 = Day 2 … 처럼 한 칸씩 밀려 있음에 주의.
 *  - 주의: 다른 파일의 regularTopics(수업 주제 배열)와 "같은 인덱스끼리" 짝지어 쓰므로,
 *    여기에 날짜를 추가/삭제하면 regularTopics도 똑같이 맞춰야 짝이 틀어지지 않는다.
 */
// 배열 순서가 곧 Day 번호(인덱스 0 = Day 1). regularTopics 등 다른 배열과
// 인덱스로 짝지어지므로, 항목을 추가/삭제할 때 순서·개수를 반드시 함께 맞춰야 한다.
export const REGULAR_DATES: string[] = [
  '2026-06-01', // Day 1   (월)
  '2026-06-02', // Day 2   (화)  ← 오늘
  '2026-06-04', // Day 3   (목)   6/3(수) 공강
  '2026-06-05', // Day 4   (금) · 1차 기초점검일
  '2026-06-08', // Day 5   (월)
  '2026-06-09', // Day 6   (화)
  '2026-06-10', // Day 7   (수)
  '2026-06-11', // Day 8   (목)
  '2026-06-12', // Day 9   (금) · 2차 학습점검일
  '2026-06-15', // Day 10  (월)
  '2026-06-16', // Day 11  (화)
  '2026-06-17', // Day 12  (수)
  '2026-06-18', // Day 13  (목)
  '2026-06-19', // Day 14  (금)
  '2026-06-22', // Day 15  (월)  ← 마지막날, 총 15일
];

// 요일 인덱스 → 한글 약어 매핑 테이블.
// Date.getDay()가 0(일)~6(토)을 반환하므로 같은 순서로 배치되어 있어야 한다.
//
// [초보자 설명]
//  - 자바스크립트의 Date 객체에서 getDay()를 부르면 "요일을 숫자로" 돌려준다.
//    규칙은 일요일=0, 월요일=1, 화요일=2 … 토요일=6 이다.
//  - 그래서 이 배열의 0번 자리에 '일', 1번 자리에 '월' … 처럼 순서를 똑같이 맞춰 두면,
//    WD[getDay()]만으로 숫자를 한글 요일로 바꿀 수 있다. (WD[2] → '화')
//  - 주의: 이 배열의 순서를 바꾸면 요일 표시가 전부 어긋나므로 절대 순서를 바꾸지 말 것.
//  - export가 없으므로 이 WD는 이 파일 안에서만 쓰는 "비공개" 도우미 값이다.
const WD = ['일', '월', '화', '수', '목', '금', '토'];

/** 'YYYY-MM-DD' → 'M/D(요일)' */
// 화면 표기용 변환기: '2026-06-05' → '6/5(금)' 형태로 짧게 보여준다.
//
// [함수 설명]
//  - 무엇을 하나: ISO 날짜 문자열을 사람이 읽기 좋은 짧은 한국어 표기로 바꾼다.
//  - 왜 이렇게 하나: '2026-06-05'를 그대로 화면에 쓰면 길고 딱딱하니, '6/5(금)'처럼 줄여 보여주려고.
//  - 매개변수 iso: 'YYYY-MM-DD' 형태의 날짜 문자열 (예: '2026-06-05').
//  - 반환값: 'M/D(요일)' 형태의 문자열 (예: '6/5(금)').
//  - 부수효과: 없음(입력만 변환해 새 문자열을 돌려주는 순수 함수).
export const fmtKDate = (iso: string): string => {
  // 문자열을 '-'로 분해 후 숫자화. 첫 요소(연도)는 사용하지 않으므로 구조분해에서 건너뛴다.
  // 예: '2026-06-05' → [, 6, 5] → m=6, d=5 (앞자리 0은 Number 변환으로 제거됨)
  //
  // [한 줄씩 풀이]
  //  1) iso.split('-')        : '2026-06-05'를 '-' 기준으로 잘라 ['2026','06','05'] 배열을 만든다.
  //  2) .map(Number)          : 각 조각에 Number()를 적용해 문자열→숫자로 바꾼다 → [2026, 6, 5].
  //                             ('06'처럼 앞에 0이 붙은 문자열도 Number를 거치면 6이 된다.)
  //  3) const [, m, d] = ...  : "구조 분해 할당". 배열 값을 변수 여러 개에 한 번에 나눠 담는 문법.
  //                             맨 앞 쉼표(,)는 "첫 값(연도 2026)은 안 쓰고 건너뛴다"는 뜻이다.
  //                             결과적으로 m=6(월), d=5(일)이 된다.
  const [, m, d] = iso.split('-').map(Number);
  // 시간대 이슈 회피를 위해 'T00:00:00'을 붙여 로컬 자정으로 파싱한 뒤 요일을 얻는다.
  // (날짜만 주면 UTC로 해석되어 하루 밀릴 수 있는 엣지케이스를 방지)
  //
  // [왜 'T00:00:00'을 붙이나? — 흔한 함정]
  //  - new Date('2026-06-05')처럼 "날짜만" 주면 자바스크립트는 이를 UTC(세계 표준시) 자정으로 해석한다.
  //    한국은 UTC보다 9시간 빠르므로, 이 값을 우리 지역 시간으로 보면 전날 오후가 되어 "하루 밀릴" 수 있다.
  //  - 반대로 'YYYY-MM-DDT00:00:00'처럼 시간까지 붙이면 "내 컴퓨터(로컬) 시간대의 자정"으로 해석되어
  //    날짜가 밀리지 않는다. 그래서 안전하게 'T00:00:00'을 덧붙인 것이다.
  //  - `${iso}T00:00:00` 은 "템플릿 리터럴": 백틱(``) 안에서 ${ } 로 변수 값을 문자열에 끼워 넣는 문법.
  //  - 마지막에 .getDay()로 요일 숫자(0~6)를 얻고, 그 숫자를 WD[...]에 넣어 한글 요일로 바꾼다.
  const wd = WD[new Date(`${iso}T00:00:00`).getDay()];
  // 'M/D(요일)' 형태의 최종 문자열을 만들어 돌려준다. 예: m=6, d=5, wd='금' → '6/5(금)'
  return `${m}/${d}(${wd})`;
};

/** 오늘 날짜를 'YYYY-MM-DD'(로컬)로 */
// "오늘" 강조 표시 등에서 REGULAR_DATES 항목과 문자열로 직접 비교하기 위한 헬퍼.
// toISOString()은 UTC 기준이라 자정 부근에 날짜가 틀어질 수 있어, 로컬 값으로 직접 조립한다.
//
// [함수 설명]
//  - 무엇을 하나: 지금 이 순간의 "오늘 날짜"를 'YYYY-MM-DD' 문자열로 만들어 돌려준다.
//  - 왜 이렇게 하나: REGULAR_DATES 안의 값들도 'YYYY-MM-DD' 문자열이라, 형식을 똑같이 맞춰야
//    todayISO() === REGULAR_DATES[i] 같은 "문자열 비교"로 오늘 수업을 쉽게 찾을 수 있기 때문이다.
//  - 매개변수: 없음.
//  - 반환값: 'YYYY-MM-DD' 형태의 문자열 (예: '2026-06-07').
//  - 부수효과: 없음. 다만 호출 시점의 "현재 시각"을 읽으므로, 같은 코드라도 날마다 결과가 달라진다.
export const todayISO = (): string => {
  // new Date() 는 "지금 이 순간"의 날짜·시간 정보를 담은 Date 객체를 만든다.
  const n = new Date();
  // 월/일을 항상 두 자리로 맞추는 0-패딩 함수(예: 6 → '06').
  //
  // [풀이] padStart(2, '0') 은 "문자열 길이가 2가 될 때까지 앞을 '0'으로 채워라"는 뜻.
  //  - String(6) → '6' (길이 1) → padStart(2,'0') → '06'
  //  - String(12) → '12' (이미 길이 2) → 그대로 '12'
  //  - 이렇게 자릿수를 맞춰야 '2026-6-5'가 아니라 '2026-06-05'처럼 ISO 형식이 정확해진다.
  const p = (x: number) => String(x).padStart(2, '0');
  // getMonth()는 0부터 시작하므로 +1 보정이 필요하다.
  //
  // [한 줄씩 풀이 — 흔한 실수 주의]
  //  - n.getFullYear() : 연도를 네 자리 숫자로 돌려준다 (예: 2026).
  //  - n.getMonth()    : 월을 돌려주는데 주의! 1월이 0, 2월이 1 … 12월이 11이다.
  //                      그래서 사람이 쓰는 달로 바꾸려면 반드시 +1 을 해야 한다. (실수 단골 포인트)
  //  - n.getDate()     : '일(day)'을 돌려준다 (1~31). 참고로 위의 getDay()(요일)와 헷갈리지 말 것.
  //  - 위 셋을 p()로 0-패딩한 뒤 '-'로 이어 붙여 'YYYY-MM-DD' 문자열을 완성한다.
  return `${n.getFullYear()}-${p(n.getMonth() + 1)}-${p(n.getDate())}`;
};
