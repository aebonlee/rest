/* =============================================================================
 * projectTopics.ts
 * -----------------------------------------------------------------------------
 * 역할:
 *   프로젝트 주제 투표 기능에서 사용하는 "운영자 제공 기본 주제(프리셋)" 정적
 *   데이터를 정의하는 모듈. 외부 API/DB 의존 없이 앱 번들에 포함되는 상수 데이터.
 *
 * 핵심 책임:
 *   - 투표 대상이 되는 7종 기본 프로젝트 주제 목록을 단일 소스로 제공.
 *   - 각 주제의 식별자(key)/제목(title)/설명(description) 스키마(PresetTopic) 정의.
 *   - 프리셋 여부를 빠르게 판별할 수 있도록 key 집합(PRESET_KEYS)을 파생 제공.
 *
 * 주요 export:
 *   - PresetTopic  : 기본 주제 한 건의 타입 정의(인터페이스).
 *   - PRESET_TOPICS: 기본 주제 7종 배열(투표 화면 렌더링·기본 데이터 소스).
 *   - PRESET_KEYS  : PRESET_TOPICS의 key만 모은 Set(프리셋 판별용, O(1) 조회).
 *
 * -----------------------------------------------------------------------------
 * [초보자를 위한 배경 지식]
 *   - 이 파일은 ".ts"(TypeScript) 파일입니다. TypeScript는 JavaScript에 "타입
 *     (값의 모양/종류)"을 더한 언어로, 잘못된 데이터를 미리 걸러내 줍니다.
 *   - "정적(static) 데이터"란? 프로그램이 실행되는 동안 바뀌지 않는, 미리 정해 둔
 *     고정 데이터를 말합니다. 서버에서 받아오지 않고 코드 안에 박아 두기 때문에
 *     인터넷이 없어도 항상 그대로 쓸 수 있습니다.
 *   - "프리셋(preset)"이란? 운영자가 미리 준비해 둔 기본값입니다. 여기서는 사용자가
 *     투표할 수 있는 "기본 후보 주제 7개"를 의미합니다.
 *   - "export"란? 이 파일에서 만든 것을 다른 파일에서 가져다(import) 쓸 수 있게
 *     "내보내기" 하는 키워드입니다. export가 없으면 이 파일 안에서만 쓸 수 있습니다.
 *   - "번들(bundle)"이란? 여러 소스 파일을 묶어 브라우저가 읽을 수 있는 하나의
 *     덩어리로 합친 결과물입니다. 이 상수 데이터도 번들 안에 함께 포함됩니다.
 *
 *   참고: 이 파일에는 React 컴포넌트나 useState/useEffect 같은 훅, async/await,
 *        정규식 등은 등장하지 않습니다. 순수하게 "데이터 + 타입 정의"만 있는
 *        가장 단순한 형태의 모듈이라 안심하고 읽어도 됩니다.
 * ============================================================================= */

/** 운영자 제공 기본 프로젝트 주제 7종 (투표 대상) */
// PresetTopic: 투표 대상 기본 주제 한 건의 형태(모양)를 정의하는 타입.
//   - "interface"란? TypeScript에서 "객체(object)가 어떤 속성을 가져야 하는지"를
//     적어 두는 설계도입니다. 이 설계도와 다른 모양의 객체를 넣으면(예: title 누락)
//     컴파일 단계에서 에러로 잡아 줘서 실수를 미리 막아 줍니다.
//   - 각 속성 뒤의 ": string"은 "이 값은 문자열이어야 한다"는 타입 표기입니다.
//   - key        : 주제 고유 식별자(예: 'p1'). 투표 집계/프리셋 판별 키로 사용.
//                  (식별자 = 다른 주제와 구별하기 위한 변하지 않는 이름표)
//   - title      : 화면에 노출되는 주제 제목.
//   - description: 주제에 대한 한 줄 설명.
export interface PresetTopic {
  key: string;
  title: string;
  description: string;
}

// PRESET_TOPICS: 운영자가 사전 정의한 기본 주제 목록(투표 옵션의 기본값).
//   - ": PresetTopic[]" 표기는 "이 변수는 PresetTopic 객체들의 배열이다"라는 뜻.
//     ([]는 배열을 의미. 즉 위 설계도(PresetTopic)를 따르는 항목들의 목록입니다.)
//   - 정적 상수이므로 부수효과 없음. 컴포넌트에서 import 하여 그대로 렌더링.
//     (부수효과 = 화면을 바꾸거나 파일을 저장하는 등 "값 계산 외의 동작". 여기엔 없음)
//   - "const"로 선언하면 PRESET_TOPICS라는 이름에 다른 배열을 다시 대입할 수 없습니다.
//     (주의: const는 "재대입 금지"일 뿐, 배열 내부 항목을 바꾸는 것까지 막지는 않습니다.
//      이 데이터는 기본값이므로 실행 중에 함부로 내부를 바꾸지 않는 것이 안전합니다.)
//   - 주의: key는 'p1'~'p7'로 고정되어 있으며, 이 값을 바꾸면 이미 저장된 기존 투표
//     데이터와 연결이 끊겨(정합성 깨짐) 집계가 어긋날 수 있으니 함부로 수정하지 마세요.
export const PRESET_TOPICS: PresetTopic[] = [
  // 아래 한 줄 한 줄이 PresetTopic 설계도를 따르는 "주제 한 건"입니다.
  // 중괄호 { } 하나가 객체 하나이고, 그 객체들을 대괄호 [ ] 안에 나열한 것이 배열입니다.
  { key: 'p1', title: '한국형 AI 동화책 제작 앱', description: '한국 정서를 담은 창작 동화와 삽화를 AI로 생성하는 앱' },
  { key: 'p2', title: '문화재 AI 해설 앱', description: '사진·위치로 문화재를 인식해 AI가 해설해 주는 앱' },
  { key: 'p3', title: '나이대별 한국사 학습·시험 앱', description: '연령대별 난이도로 한국사를 학습하고 시험까지 보는 앱' },
  { key: 'p4', title: '자격증 취약점 분석 학습 앱', description: '학습 데이터로 자격증 취약 영역을 분석·보완해 주는 앱' },
  { key: 'p5', title: '청년지원정책 안내 챗봇', description: '조건을 입력하면 맞춤 청년지원정책을 안내하는 챗봇' },
  { key: 'p6', title: 'AI 자기소개서·면접 코치', description: '자기소개서 첨삭과 모의면접을 돕는 AI 코치' },
  { key: 'p7', title: '회복탄력성 루틴 코치', description: '멘탈 회복을 돕는 맞춤 루틴·습관 코칭 앱' },
];

// PRESET_KEYS: PRESET_TOPICS에서 key만 추출해 만든 Set(집합).
//   - "Set"이란? 중복 없는 값들의 모음입니다. 배열과 비슷하지만 "이 값이 들어 있나?"를
//     매우 빠르게 확인할 수 있는 것이 장점입니다.
//   - "O(1) 조회"란? 데이터가 아무리 많아도 "들어 있는지" 확인하는 데 걸리는 시간이
//     거의 일정하다는 뜻입니다. 배열로 일일이 훑으면(데이터가 많을수록 느려짐) 대신
//     Set의 has(...)를 쓰면 항상 빠릅니다. (예: PRESET_KEYS.has('p1') → true)
//   - 용도: 임의의 주제 key가 "운영자 기본 주제(프리셋)"인지 빠르게 판별.
//     (예: 사용자가 추가한 커스텀 주제와 프리셋 주제를 구분할 때 사용)
//   - 동작 풀이: PRESET_TOPICS.map((t) => t.key)
//       · map은 배열의 각 항목을 변환해 "새 배열"을 만드는 함수입니다.
//       · (t) => t.key 는 "주제 객체 t를 받아 그 key만 꺼낸다"는 짧은 함수(화살표 함수).
//       · 결과: ['p1', 'p2', ..., 'p7'] 같은 key들의 배열이 만들어지고,
//         그 배열을 new Set(...)에 넣어 중복 없는 집합으로 변환합니다.
//   - PRESET_TOPICS에서 파생되므로 위 배열을 수정하면 이 Set도 자동으로 동기화됨.
//     (즉 key를 추가/삭제하면 따로 손댈 필요 없이 PRESET_KEYS에 그대로 반영됩니다.)
export const PRESET_KEYS = new Set(PRESET_TOPICS.map((t) => t.key));
